def draw_pic(title, datas: list, xLabel, yLabel):
    plot_datas = []
    for data in datas:
        plot_data = {
            "x": data['plotsX'],
            "y": data['plotsY'],
            "label": data["label"]
        }
        plot_datas.append(plot_data)

    for plot_data in plot_datas:
        # list.sort(plot_data["x"])
        # list.sort(plot_data["y"])
        plt.plot(plot_data["x"], plot_data["y"], label=plot_data["label"])

    plt.ylabel(yLabel)
    plt.xlabel(xLabel)

    plt.title(title)

    plt.legend()
    plt.show()


